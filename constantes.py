# Una constante no es más que una variable cuyo valor no cambia durante la ejecución del programa.
# Se declara de la misma forma que una variable, pero usando mayúsculas y guiones bajos para separar palabras.
# <ETIQUETA_DE_CONSTANTE> = <VALOR>

